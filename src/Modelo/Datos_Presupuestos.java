    private String CATEGORIA;
    private Double MONTO_PRESUPUESTO;
    private int ID_FECHA;
    private String TIPO;

    public int getID_PRESUPUESTOS() {
        return ID_PRESUPUESTOS;
    }

    public void setID_PRESUPUESTOS(int ID_PRESUPUESTOS) {
        this.ID_PRESUPUESTOS = ID_PRESUPUESTOS;
    }

    public String getCATEGORIA() {
        return CATEGORIA;
    }

    public void setCATEGORIA(String CATEGORIA) {
        this.CATEGORIA = CATEGORIA;
    }

    public Double getMONTO_PRESUPUESTO() {
        return MONTO_PRESUPUESTO;
    }

    public void setMONTO_PRESUPUESTO(Double MONTO_PRESUPUESTO) {
        this.MONTO_PRESUPUESTO = MONTO_PRESUPUESTO;
    }

    public int getID_FECHA() {
        return ID_FECHA;
    }

    public void setID_FECHA(int ID_FECHA) {
        this.ID_FECHA = ID_FECHA;
    }

    public String getTIPO() {
        return TIPO;
    }

    public void setTIPO(String TIPO) {
        this.TIPO = TIPO;
    }
    
    
    
    
    
    
    
    
